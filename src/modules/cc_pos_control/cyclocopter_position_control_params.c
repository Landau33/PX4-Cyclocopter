/**
 * 悬停所需的垂直推力
 *
 * 在自稳模式下映射到油门操纵杆的中心位置（参见 CPC_THR_CURVE）。
 * 用于悬停推力估计器的初始化（参见 CPC_USE_HTE）。
 * 估计的悬停推力用作高度控制中零垂直加速度的基础。
 * 悬停推力对于正确的工作着陆检测非常重要。
 *
 * @单位 规范化 (norm)
 * @最小值 0.1
 * @最大值 0.8
 * @小数位 2
 * @增量 0.01
 */
PARAM_DEFINE_FLOAT(CPC_THR_HOVER, 0.5f);

/**
 * 悬停推力估计器
 *
 * 禁用以使用固定的参数 CPC_THR_HOVER
 * 启用以使用悬停推力估计器
 *
 * @布尔值
 */
PARAM_DEFINE_INT32(CPC_USE_HTE, 1);

/**
 * 水平推力裕度
 *
 * 当更高优先级的垂直推力饱和时，为水平控制保留的裕度。
 * 以避免在高垂直误差情况下完全剥夺水平控制。
 *
 * @单位 规范化 (norm)
 * @最小值 0
 * @最大值 0.5
 * @小数位 2
 * @增量 0.01
 */
PARAM_DEFINE_FLOAT(CPC_XY_THR_MARG, 0.3f);

/**
 * 数值速度导数低通截止频率
 *
 * @单位 赫兹 (Hz)
 * @最小值 0
 * @最大值 10
 * @小数位 1
 * @增量 0.5
 */
PARAM_DEFINE_FLOAT(CPC_VELD_LP, 5.0f);
